package com.velvet.vo;




public class PartsSearchResultsBean{
	
	private String partsName;
	private String partsCode;
	private String partsQuantity;
	private String operation;
	private String createdOn;
	private String thresholdQuantity;
	
	public String getThresholdQuantity() {
		return thresholdQuantity;
	}
	public void setThresholdQuantity(String thresholdQuantity) {
		this.thresholdQuantity = thresholdQuantity;
	}
	public String getPartsName() {
		return partsName;
	}
	public void setPartsName(String partsName) {
		this.partsName = partsName;
	}
	public String getPartsCode() {
		return partsCode;
	}
	public void setPartsCode(String partsCode) {
		this.partsCode = partsCode;
	}
	public String getPartsQuantity() {
		return partsQuantity;
	}
	public void setPartsQuantity(String partsQuantity) {
		this.partsQuantity = partsQuantity;
	}
	public String getOperation() {
		return operation;
	}
	public void setOperation(String operation) {
		this.operation = operation;
	}
	public String getCreatedOn() {
		return createdOn;
	}
	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}

	
	
}
